            <h5>《太上老君内观经》</h5>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;君曰： “ 天地媾精，陰陽布化，万物以生。承其宿业，分灵道一。父母和合，人受其生。始，一月为胞，精血凝也。二月成胎，形兆胚也。三月陽神为三魂，动而生也。四月陰灵为七魄，静镇形也。五月，五行分藏以安神也。六月，六律定腑用滋灵也。七月，七精开窍，通光明也。八月，八景神具，降真灵也。九月，宫室罗布，以定精也。十月气足，万象成也。元和哺食，时不停也。 太一帝君在头，曰泥丸君，总众神也。照生识神，人之魂也。司命处心，纳生元也。无英居左，制三魂也。白元居右，拘七魄也。桃孩住脐，保精根也。照诸百节，生百神也。所以周身，神不空也。元气入鼻，灌泥丸也。所以神明，形固安也。运动住止，关其心也。所以谓生，有由然也。予内观之，历历分也。
                心者，禁也，一身之主。心能禁制，使形神不邪也。心则神也，变化不测，故无定形。所以五藏藏五神，魂在肝，魄在肺，精在肾，志在脾，神在心，所以字殊，随处名也。心者，火也，南方太陽之精，主火。上为荧惑，下应心也。色赤，三叶如莲花，神明依泊，从所名也。其神也，非青非黄，非大非小，非短非长，非曲非直，非柔非刚，非厚非薄，非圆非方。变化莫测，混合陰陽。大包天地，细入毫芒。制之则正，放之则狂。清净则生，浊躁则亡。明照八表，暗迷一方。但能虚寂，生道自常。永保无为，其身则昌也。以其无形，莫之能名，祸福吉凶，悉由之矣。所以圣人，立君臣，明赏罚。置官僚，制法度，正以教人。人以难伏，唯在于心，心若清净，则万祸不生。所以流浪生死，沈沦恶道，皆由心也。妄想憎爱，取舍去来，染着聚结，渐自缠绕，转转系缚，不能解脱，便至灭亡。由如牛马，引重趋泥，转增陷没，不能自出，遂至于死。人亦如是，始生之时，神元清静，湛然无杂。既受纳有，形染六情，眼则贪色，耳则殢声，口则耽味，鼻则受馨，意随健羡，身欲肥轻，从此流浪，莫能自悟。圣人慈念，设法教化，使内观己身，澄其心也。
                ” 老君曰： “ 谛观此身，从虚无中来。因缘运会，积精聚气，乘业降神，和合受生，法天像地，含陰吐陽，分错五行，以应四时。眼为日月，发为星辰，眉为华盖，头为昆仑，布列宫阙，安置精神。万物之中，人最为灵。性命合道，人当爱之。内观其身，惟人尊焉。而不自贵，妄染诸尘，不净臭秽，浊乱形神。熟观物我，何疏何亲。守道长生，为善保真。世愚役役，徒自苦辛也。 ” 老君曰： “ 从道受分，谓之命；自一禀形，谓之性。所以任物，谓之心。心有所忆，谓之意。意之所出，谓之志。事无不知，谓之智。智周万物，谓之慧。动而营身，谓之魂。静而镇形，谓之魄。流行骨肉，谓之血。保神养气，谓之精。气清而驶，谓之荣。气浊而迟，谓之卫。总括百神，谓之身。万象备见，谓之形。块然有阂，谓之质。状貌可则谓之体，大小有分谓之躯。众思不测，谓之神。邈然应化，谓之灵。气来入身，谓之生。神去于身，谓之死。所以通生，谓之道。道者，有而无形，无而有情，变化不测，通神群生。在人之身，则为神明，所谓心也。所以教人修道，则修心也。教人修心，则修道也。道不可见，因生而明之。生不可常，用道以守之。若生亡则道废，道废则生亡。生道合一，则长生不死，羽化神仙。人不能长保者，以其不能内观于心故也。内观不遗，生道长存。
                ” 老君曰： “ 人所以流浪恶道，沈沦滓秽，缘六情起妄，而生六识，六识分别，系缚憎爱，去来取舍，染着烦恼，与道长隔。所以内观六识，因起六欲。欲从何起？欲自识起。识从何起？识自欲起。妄想颠倒，而生有识。亦曰自然，又名无为。本来虚静，元无有识。有识分别，起诸邪见。邪见既兴，尽是烦恼。展转系缚，流浪生死，永失于道矣。 ” 老君曰： “ 道无生死，而形有生死。所以言生死者，属形不属道也。形所以生者，由得其道也。形所以死者，由失其道也。人能存生守道，则长存不亡也。又曰：人能常清静其心，则道自来居。道自来居，则神明存身。神明存身，则生不亡也。人常欲生，而不能虚心。人常恶死，而不能保神。亦犹欲贵，而不用道；欲富，而不求宝；欲速，而足不行；欲肥，而食不饱也。
                ” 老君曰： “ 道以心得，心以道明。心明则道降，道降则心通。神明之在身，犹火之因卮也。明从火起，火自炷发。炷因油润，油藉卮停。四者若废，明何生焉。亦如明缘神照，神托心存。水由形有，形以道全。一物不足，明何依焉？所以谓之神明者，眼见耳闻，意知心觉，分别物理，细微悉知，由神以明，故曰神明也。 ” 老君曰： “ 所以言虚心者，遣其实也；无心者，除其有也；定心者，令不动也；安心者，使不危也；静心者，令不乱也；正心者，使不邪也；清心者，使不浊也；净心者，使不秽也。此皆以有，令使除也。四见者，心直者，不反复也。心平者，无高低也。心明者，无暗昧也。心通者，无窒碍也。此皆本自照者也。粗言数语，余可思也。
                ” 老君曰： “ 知道易，信道难。信道易，行道难。行道易，得道难。得道易，守道难。守而不失，乃常存也。 ” 老君曰： “ 道也者，不可言传口授而得之。常虚心静神，道自来居。愚者不知，劳其形，苦其心，役其志，躁其神，而道愈远，而神愈悲。背道求道，当慎择焉。 ” 老君曰： “ 道贵长存，保神固根。精气不散，纯白不分。形神合道，飞升昆仑。先天以生，后天以存。出入无间，不由其门。吹陰煦陽，制魄拘魂。亿岁眷属，千载子孙。黄尘四起，骑羊真人。金堂玉室，送故迎新。 ” 老君曰：
                “ 内观之道，静神定心。乱想不起，邪妄不侵。固身及物，闭目思寻。表里虚寂，神道微深。外藏万境，内察一心。了然明静，静乱俱息。念念相系，深根宁极。湛然常住，杳冥难测。忧患永消，是非莫识。 ” 老君曰： “ 吾非圣人，学而得之。故我求道，无不受持。千经万术，惟在心也。 ”</p>
